let frutas = ["manzana", "pera", "uva", "naranja", "sandia"];

frutas.push("piña"); //agrega elemenos al final
console.log(frutas);


//eleimindando el ultimo elemento
frutas.pop();
console.log(frutas);

//agregar un elemento al inicio
frutas.unshift("kiwi");
console.log(frutas);

//eliminar el primer elemento   
frutas.shift();
console.log(frutas);


